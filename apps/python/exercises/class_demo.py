# ==============================================================
# 【抽象类】
# ==============================================================
# 抽象类是不能直接实例化的类，它定义了一些抽象方法（没有实现的占位符）
# 子类必须实现这些抽象方法才能实例化
#
# 抽象类的用途：
#   - 为一组类定义统一的接口/规范
#   - 强制子类实现特定的方法
#   - 提供基础实现，让子类继承和扩展
#
# 相关概念：
#   - ABC（Abstract Base Class）：抽象基类
#   - @abstractmethod：标记抽象方法的关键装饰器
# ==============================================================

print("\n=== 抽象类 ===")

from abc import ABC, abstractmethod


# ----------------------------------------------------------
# 抽象类定义
# ----------------------------------------------------------
# Shape 继承自 ABC，因此成为一个抽象类
# 抽象类可以包含：
#   - 抽象方法：用 @abstractmethod 装饰，没有实现（只有 pass 或 ...）
#   - 具体方法：有实现的普通方法，可以有自己的默认行为
# ----------------------------------------------------------
class Shape(ABC):
    """抽象形状类（定义几何形状的通用接口）"""

    # ----------------------------------------------------------
    # 抽象方法
    # ----------------------------------------------------------
    # 子类必须实现这些方法，否则无法实例化
    # 抽象方法没有具体的实现代码，只有 pass 或 ...
    # ----------------------------------------------------------
    @abstractmethod
    def area(self):
        """计算面积（子类必须实现）"""
        pass

    @abstractmethod
    def perimeter(self):
        """计算周长（子类必须实现）"""
        pass

    # ----------------------------------------------------------
    # 具体方法
    # ----------------------------------------------------------
    # 抽象类可以有非抽象的方法实现
    # 子类可以继承这个方法，也可以重写
    # ----------------------------------------------------------
    def info(self):
        """返回形状的信息（使用抽象方法计算）"""
        return f"面积: {self.area()}, 周长: {self.perimeter()}"


# ----------------------------------------------------------
# 实现抽象类
# ----------------------------------------------------------
# Rectangle 是具体类，它必须实现所有抽象方法
# 才能被实例化
# ----------------------------------------------------------
class Rectangle(Shape):
    """矩形类：具体实现 Shape 抽象类"""

    def __init__(self, width, height):
        # 矩形的宽和高
        self.width = width
        self.height = height

    def area(self):
        """矩形面积 = 宽 × 高"""
        return self.width * self.height

    def perimeter(self):
        """矩形周长 = 2 × (宽 + 高)"""
        return 2 * (self.width + self.height)


class Circle(Shape):
    """圆类：具体实现 Shape 抽象类"""

    def __init__(self, radius):
        # 圆的半径
        self.radius = radius

    def area(self):
        """圆面积 = π × 半径²"""
        return 3.14159 * self.radius**2

    def perimeter(self):
        """圆周长 = 2 × π × 半径"""
        return 2 * 3.14159 * self.radius


# 创建具体类的实例
r = Rectangle(4, 5)
c = Circle(3)

print(f"矩形: {r.info()}")  # 输出: 矩形: 面积: 20, 周长: 18
print(f"圆形: {c.info()}")  # 输出: 圆形: 面积: 28.27..., 周长: 18.84...

