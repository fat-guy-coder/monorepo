class Positive:
    """确保属性值为正数的描述符"""

    def __init__(self):
        # 使用字典存储每个实例的值
        # 键是实例对象，值是该实例的属性值
        # 这样每个实例都有独立的值
        self.values = {}

    # ----------------------------------------------------------
    # __get__ 方法
    # ----------------------------------------------------------
    # 当访问属性时调用
    # instance：拥有该属性的实例（如果是类访问则为 None）
    # owner：拥有该属性的类
    # ----------------------------------------------------------
    def __get__(self, instance, owner):
        # 如果实例没有值，返回 0（默认值）
        return self.values.get(instance, 0)

    # ----------------------------------------------------------
    # __set__ 方法
    # ----------------------------------------------------------
    # 当设置属性时调用
    # ..............
    # ----------------------------------------------------------
    def __set__(self, instance, value):
        # 验证值必须为正数
        if value < 0:
            raise ValueError("值必须为正数")
        # 存储到字典中，以实例为键
        self.values[instance] = value

    # 如果需要，可以实现 __delete__ 方法控制属性删除


