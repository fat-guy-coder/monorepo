# 【描述符（Descriptor）】
# ==============================================================
# 描述符是一种对象，作为另一个类的属性存在时，可以控制属性的访问、修改和删除
# 通过实现以下方法之一来创建描述符：
#   - __get__(self, instance, owner): 获取属性时调用
#   - __set__(self, instance, value): 设置属性时调用
#   - __delete__(self, instance): 删除属性时调用
#
# 数据描述符：同时实现了 __get__ 和 __set__（或 __delete__）
# 非数据描述符：只实现了 __get__
#
# 优先级（从高到低）：
#   1. 数据描述符（__get__ + __set__）
#   2. 实例对象的 __dict__
#   3. 非数据描述符（只有 __get__）
#   4. 类属性
# ==============================================================

