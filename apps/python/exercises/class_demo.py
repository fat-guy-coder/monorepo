# 【元类（Metaclass）】
# ==============================================================
# 元类是"创建类的类"（Class of Classes）
# 正常情况下，类是 type 的实例；但元类可以是 type 的子类
#
# 默认情况下，Python 类由 type 创建：
#   - class MyClass: -> MyClass = type('MyClass', (), {})
#
# 使用元类可以：
#   - 在类创建时修改类的行为
#   - 拦截类的创建过程
#   - 添加额外的属性或方法
#   - 实现单例模式、注册机制等
#
# 语法：class MyClass(metaclass=MyMeta):
# ==============================================================

print("\n=== 元类 ===")


# ----------------------------------------------------------
# 定义元类
# ----------------------------------------------------------
# 元类继承自 type
# 必须实现 __new__ 方法来控制类的创建
# ----------------------------------------------------------
class Meta(type):
    """自定义元类，继承自 type"""

    # ----------------------------------------------------------
    # __new__ 方法
    # ----------------------------------------------------------
    # 在类创建时自动调用
    # 参数：
    #   - mcs: 元类本身（即 Meta）
    #   - name: 要创建的类名（字符串）
    #   - bases: 父类元组
    #   - attrs: 类的属性字典
    # 返回值：创建的类对象
    # ----------------------------------------------------------
    def __new__(mcs, name, bases, attrs):
        # 在类创建之前，向属性字典中添加一个类属性
        attrs["created_by"] = "Meta"
        # 调用父类的 __new__ 完成创建
        return super().__new__(mcs, name, bases, attrs)


# ----------------------------------------------------------
# 使用元类创建类
# ----------------------------------------------------------
# metaclass=Meta 指定 MyClass 由 Meta 创建
# ----------------------------------------------------------
class MyClass(metaclass=Meta):
    """使用元类创建"""

    # 此类在创建时会自动添加 created_by 属性
    pass


# 通过元类创建的类会包含元类添加的属性
print(f"MyClass.created_by: {MyClass.created_by}")  # 输出: Meta


# ==============================================================
# 【混入（Mixin）模式】
# ==============================================================
# Mixin 是一种设计模式，用于在不使用继承的情况下向类添加功能
# 在 Python 中，Mixin 就是一个提供方法但不定义实例属性的类
# 它通过多继承的方式被"混入"到其他类中
#
# Mixin 的特点：
#   - 通常不定义 __init__（或只是 pass）
#   - 提供一些有用的方法
#   - 通过多继承被其他类使用
#   - 命名通常以 Mixin 结尾
#
# 使用场景：
#   - 当多个类需要相同的功能，但它们没有"is-a"关系
#   - 当想要组合多个可选功能时
# ==============================================================

print("\n=== Mixin 混入 ===")


# ----------------------------------------------------------
# LogMixin：日志混入
# ----------------------------------------------------------
# 提供日志记录功能
# ----------------------------------------------------------
class LogMixin:
    """日志混入类"""

    def log(self, msg):
        """记录日志消息"""
        # 格式化日志输出
        print(f"[LOG] {msg}")


# ----------------------------------------------------------
# SerializableMixin：序列化混入
# ----------------------------------------------------------
# 提供对象序列化功能
# ----------------------------------------------------------
class SerializableMixin:
    """序列化混入类"""

    def to_dict(self):
        """将对象的公开属性转换为字典"""
        result = {}
        # 遍历对象的 __dict__（实例属性）
        for k, v in self.__dict__.items():
            # 排除以 _ 开头的私有属性
            if not k.startswith("_"):
                result[k] = v
        return result


# ----------------------------------------------------------
# 使用 Mixin
# ----------------------------------------------------------
# User 同时继承了 LogMixin 和 SerializableMixin
# 这样 User 就同时拥有了日志和序列化功能
# ----------------------------------------------------------
class User(LogMixin, SerializableMixin):
    """用户类，同时具有日志和序列化功能"""

    def __init__(self, name, email):
        # 普通的实例属性
        self.name = name
        self.email = email


# 创建用户实例
u = User("Alice", "alice@example.com")

# 调用 Mixin 提供的方法
u.log("用户已创建")  # 输出: [LOG] 用户已创建
print(
    f"用户数据: {u.to_dict()}"
)  # 输出: {'name': 'Alice', 'email': 'alice@example.com'}


# ==============================================================
# 【组合 vs 继承】
# ==============================================================
# 组合（Composition）和继承（Inheritance）都是代码复用方式
# 但现代面向对象设计更倾向于"优先使用组合"
#
# 继承（Inheritance）：
#   优点：简洁，语法简单，自动获得父类功能
#   缺点：耦合度高，父类变化影响所有子类，不够灵活
#
# 组合（Composition）：
#   优点：松耦合，可以动态替换组件，易于测试
#   缺点：需要显式委托，需要更多代码
#
# 设计原则：
#   - "组合优先于继承"（Favor composition over inheritance）
#   - "继承是一种强耦合关系"
#   - 只有当确实存在"is-a"关系时才使用继承
# ==============================================================

print("\n=== 组合（优先使用组合）===")

# ----------------------------------------------------------
# 组件类
# ----------------------------------------------------------
# 这些类是独立的组件，可以被任何类使用
# ----------------------------------------------------------


class Engine:
    """引擎组件"""

    def start(self):
        """启动引擎"""
        return "引擎启动"


class Wheel:
    """轮子组件"""

    def roll(self):
        """轮子滚动"""
        return "轮子滚动"


# ----------------------------------------------------------
# 组合类
# ----------------------------------------------------------
# Car 不继承 Engine 和 Wheel，而是"拥有"它们
# 这就是"组合"——通过包含其他对象来实现功能
# ----------------------------------------------------------
class Car:
    """汽车类，使用组合而非继承"""

    def __init__(self):
        # 在构造方法中创建组件实例
        # 这就是" HAS-A "关系：Car HAS-A Engine, Car HAS-A Wheels
        self.engine = Engine()
        # 创建四个轮子
        self.wheels = [Wheel() for _ in range(4)]

    def drive(self):
        """驾驶汽车"""
        # 组合各个组件的行为
        parts = [self.engine.start()] + [w.roll() for w in self.wheels]
        return ", ".join(parts)


# 创建汽车实例
car = Car()
print(car.drive())  # 输出: 引擎启动, 轮子滚动, 轮子滚动, 轮子滚动, 轮子滚动


