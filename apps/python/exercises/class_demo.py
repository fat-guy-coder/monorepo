# 【__slots__ 限制属性】
# ==============================================================
# __slots__ 是一个特殊的类属性，用于限制实例可以拥有的属性
# 正常情况下，Python 实例通过 __dict__ 字典存储属性，__slots__ 可以禁用这个机制
#
# 用途：
#   - 节省内存：禁用 __dict__ 后，每个实例占用更少内存
#   - 限制属性：防止随意添加新属性，提高代码安全性
#
# 注意：
#   - 使用 __slots__ 的类不能有类属性默认值（除非父类也有 __slots__）
