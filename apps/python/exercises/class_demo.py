# ==============================================================
# 【类的定义与实例化】
# ==============================================================
# class 关键字用于定义类，类是一种模板，用于创建对象
# 类名通常使用 PascalCase（首字母大写）命名风格
# 类可以包含：
#   - 类属性：定义在类内部、方法外部，所有实例共享
#   - 实例属性：定义在 __init__ 方法中，每个实例独有
#   - 实例方法：第一个参数永远是 self，表示实例本身
#   - 类方法：第一个参数是 cls，表示类本身
#   - 静态方法：不依赖类或实例的方法
# ==============================================================


class Person:
    """一个人类（类的文档字符串，描述类的用途）"""

    # ----------------------------------------------------------
    # 类属性（Class Attribute）
    # ----------------------------------------------------------
    # 定义在类内部、方法外部的属性
    # 特点：所有实例共享同一个属性值
    # 修改类属性会影响所有实例
    # 通常用于定义类级别的常量或默认值
    # ----------------------------------------------------------
    species = "人类"

    # ----------------------------------------------------------
    # __init__ 方法（构造器/初始化方法）
    # ----------------------------------------------------------
    # 当创建类的实例时自动调用
    # self 参数是必须的，它指向刚刚创建的实例本身
    # 其他参数（如 name, age）在创建实例时传入
    # 在这里定义的属性称为"实例属性"，每个实例都有自己独立副本
    # ----------------------------------------------------------
    def __init__(self, name, age):
        # self.name 表示将 name 存储在实例的命名空间中
        # self.age 同理
        # 这些就是实例属性，每个 Person 实例都有独立的 name 和 age
        self.name = name
        self.age = age

    # ----------------------------------------------------------
    # 实例方法（Instance Method）
    # ----------------------------------------------------------
    # 第一个参数必须是 self
    # 可以访问和修改实例的属性
    # 可以访问类的属性和方法
    # ----------------------------------------------------------
    def say_hello(self):
        # 返回一个问候字符串，使用 f-string 格式化
        # f-string 中 {self.name} 会被替换为实际的 name 值
        return f"你好，我是{self.name}"

    # ----------------------------------------------------------
    # __str__ 方法（字符串表示）
    # ----------------------------------------------------------
    # 当使用 str() 函数或 print() 打印对象时调用
    # 应该返回一个友好的、可读性高的字符串
    # 用于面向用户的信息展示
    # ----------------------------------------------------------
    def __str__(self):
        return f"Person({self.name}, {self.age})"

    # ----------------------------------------------------------
    # __repr__ 方法（官方表示）
    # ----------------------------------------------------------
    # 当在交互式环境输出对象、或在调试时显示对象时调用
    # 应该返回一个精确、可用于重建对象的字符串
    # 约定格式：ClassName(attr1='value1', attr2=value2)
    # ----------------------------------------------------------
    def __repr__(self):
        return f"Person(name='{self.name}', age={self.age})"


# ==============================================================
# 【实例化】
# ==============================================================
# 创建类的实例，语法：实例名 = 类名(参数1, 参数2, ...)
# Python 会自动调用 __init__ 方法
# ==============================================================

# 创建第一个实例，传入 name="小明", age=20
p1 = Person("小明", 20)

# 创建第二个实例，传入 name="小红", age=18
# p1 和 p2 是两个独立的对象，有各自独立的 name 和 age
p2 = Person("小红", 18)

# print() 会调用对象的 __str__ 方法
print(p1)  # 输出: Person(小明, 20)
print(p2)  # 输出: Person(小红, 18)

# 调用实例方法
print(p1.say_hello())  # 输出: 你好，我是小明

# 访问类属性：通过实例访问
print(f"p1 的物种: {p1.species}")  # 输出: p1 的物种: 人类
print(f"p2 的物种: {p2.species}")  # 输出: p2 的物种: 人类

# ----------------------------------------------------------
# 类属性可以被所有实例修改（但不要这样做）
# ----------------------------------------------------------
# 直接修改类属性会影响所有实例
# 这种全局修改的方式容易引入bug，实践中很少使用
Person.species = "智人"  # 将类属性改为"智人"
print(f"修改后 p1 的物种: {p1.species}")  # 输出: 修改后 p1 的物种: 智人
# 注意：p2 也会显示"智人"，因为类属性被改了


# ==============================================================
# 【访问限制（封装）】
# ==============================================================
# 封装是面向对象的基本原则之一，用于隐藏内部实现细节
# Python 通过命名约定来实现封装：
#   - 公开属性：无前缀，如 name
#   - 受保护属性：单下划线前缀，如 _score（约定不应在类外访问）
#   - 私有属性：双下划线前缀，如 __grade（会被名字重整）
#
# 名字重整（Name Mangling）：
#   双下划线开头的属性会被重整为 _ClassName__attr 的形式
#   这样可以在继承时避免命名冲突，但并不是真正的私有
# ==============================================================

print("\n=== 访问限制 ===")


class Student:
    """学生类，演示访问控制"""

    def __init__(self, name, score):
        # 公开属性：可以直接访问
        self.name = name

        # 受保护属性：单下划线开头，约定外部不应直接访问
        # 但这只是约定，Python 不会阻止访问
        self._score = score

        # 私有属性：双下划线开头，会触发名字重整
        # 实际存储为 _Student__grade
        # 这是一种轻度保护机制，但不是绝对安全
        self.__grade = "A"  # 默认等级为 A

    # ----------------------------------------------------------
    # Getter 方法（访问器）
    # ----------------------------------------------------------
    # 用于安全地获取私有/受保护属性的值
    # 可以在方法中添加额外的逻辑（如验证、转换）
    # ----------------------------------------------------------
    def get_score(self):
        """获取分数"""
        return self._score

    # ----------------------------------------------------------
    # Setter 方法（修改器）
    # ----------------------------------------------------------
    # 用于安全地修改私有/受保护属性的值
    # 可以在方法中添加验证逻辑，防止非法值
    # ----------------------------------------------------------
    def set_score(self, score):
        """设置分数"""
        # 验证分数是否在合理范围内
        if 0 <= score <= 100:
            self._score = score
        else:
            # 抛出异常，提示用户输入无效
            raise ValueError("分数必须在0-100之间")

    # ----------------------------------------------------------
    # @property 装饰器
    # ----------------------------------------------------------
    # 将一个方法转换为"属性"，可以像访问属性一样调用
    # 优点：可以在访问时添加逻辑，实现只读属性等
    # 语法：@property 装饰的方法，调用时不加括号
    # ----------------------------------------------------------
    @property
    def grade(self):
        """属性装饰器 - 获取等级（只读）"""
        # 根据分数计算等级
        if self._score >= 90:
            return "A"
        elif self._score >= 80:
            return "B"
        elif self._score >= 60:
            return "C"
        else:
            return "D"


# 创建学生实例
s = Student("张三", 85)

# 访问公开属性
print(f"学生: {s.name}, 分数: {s.get_score()}")  # 通过 getter 获取分数
print(f"等级: {s.grade}")  # 通过 property 获取等级

# 通过 setter 修改分数
s.set_score(92)
print(f"修改后分数: {s.get_score()}, 等级: {s.grade}")

# ----------------------------------------------------------
# 访问私有属性（不推荐）
# ----------------------------------------------------------
# 虽然可以访问，但破坏了封装性，不建议这样做
# 这里演示名字重整后的实际属性名
print(f"私有属性(名字重整后): {s._Student__grade}")

# ----------------------------------------------------------
# property 装饰器用法
# ----------------------------------------------------------
# 直接修改 _score（绕过 getter/setter），grade 会自动重新计算
s._score = 55
print(f"通过 property 获取等级: {s.grade}")  # 输出: D


# ==============================================================
# 【继承】
# ==============================================================
# 继承是面向对象的基本特性，允许定义一个类（子类）来继承另一个类（父类）
# 语法：class 子类名(父类名)
#
# 继承的好处：
#   - 代码复用：子类可以继承父类的属性和方法
#   - 层次结构：建立类之间的"is-a"关系（如 Dog is an Animal）
#   - 多态：不同子类对同一方法有不同的实现
#
# 方法重写（Override）：
#   子类可以重写父类的方法，重新定义自己的行为
# ==============================================================

print("\n=== 继承 ===")


class Animal:
    """动物基类"""

    def __init__(self, name):
        # 每个动物都有一个名字
        self.name = name

    def speak(self):
        # 抽象方法，默认返回"..."
        # 子类应该重写此方法
        return "..."


class Dog(Animal):
    """Dog 继承自 Animal（子类）"""

    # Dog 会继承 Animal 的所有属性和方法

    def speak(self):
        # 重写父类的 speak 方法
        return "汪汪汪"


class Cat(Animal):
    """Cat 继承自 Animal（子类）"""

    def speak(self):
        # 重写父类的 speak 方法
        return "喵喵喵"


class BullDog(Dog):
    """斗牛犬 - 多层继承（继承链：BullDog -> Dog -> Animal）"""

    # 子类可以继续继承，形成继承链

    def speak(self):
        # 再次重写
        return "哼哼哼"


# 创建各类的实例
dog = Dog("旺财")
cat = Cat("咪咪")
bull = BullDog("小斗")

# 调用 speak 方法，会执行各子类重写后的版本
print(f"{dog.name}: {dog.speak()}")  # 输出: 旺财: 汪汪汪
print(f"{cat.name}: {cat.speak()}")  # 输出: 咪咪: 喵喵喵
print(f"{bull.name}: {bull.speak()}")  # 输出: 小斗: 哼哼哼


# ==============================================================
# 【多继承】
# ==============================================================
# Python 支持多继承，即一个子类可以继承多个父类
# 语法：class 子类(父类1, 父类2, 父类3)
#
# 多继承的注意点：
#   - 方法解析顺序（MRO）：Python 使用 C3 线性化算法
#   - 避免钻石继承（菱形继承）导致的歧义
#   - 谨慎使用，因为会增加代码复杂度
# ==============================================================

print("\n=== 多继承 ===")


class Flyable:
    """会飞的 mixin 类"""

    def fly(self):
        # 返回飞行描述
        return "我可以飞"


class Swimmable:
    """会游泳的 mixin 类"""

    def swim(self):
        # 返回游泳描述
        return "我可以游泳"


# Duck 继承自 Animal，同时具有 Flyable 和 Swimmable 的能力
# 这就是 Mixin 模式的应用
class Duck(Animal, Flyable, Swimmable):
    """鸭子继承自动物，同时会飞和游泳"""

    def speak(self):
        # 重写 speak 方法
        return "嘎嘎嘎"


duck = Duck("唐老鸭")

# duck 是 Animal 的实例，所以有 speak 方法
# duck 也是 Flyable 的实例，所以有 fly 方法
# duck 也是 Swimmable 的实例，所以有 swim 方法
print(f"{duck.name}: {duck.speak()}")  # 输出: 唐老鸭: 嘎嘎嘎
print(duck.fly())  # 输出: 我可以飞
print(duck.swim())  # 输出: 我可以游泳


# ==============================================================
# 【super() 调用父类方法】
# ==============================================================
# super() 函数用于调用父类的方法
# 主要用途：
#   - 在子类中调用父类的 __init__，复用父类的初始化逻辑
#   - 在子类中扩展父类的方法，而不是完全替换
#
# 语法：super().__init__(参数) 或 super(类名, self).__init__(参数)
# 后者用于多重继承中明确指定调用哪个父类
# ==============================================================

print("\n=== super() 调用父类 ===")


class Teacher(Animal):
    """教师类，继承自动物类"""

    def __init__(self, name, subject):
        # super().__init__(name) 调用父类的 __init__ 方法
        # 这样可以复用父类的初始化逻辑，而不必重复写 self.name = name
        super().__init__(name)  # 调用父类的 __init__
        self.subject = subject  # 添加教师特有的属性

    def teach(self):
        return f"教{self.subject}"

    def speak(self):
        # 重写 speak 方法，但先调用父类的方法
        return f"我是教{self.subject}的老师"


t = Teacher("李老师", "数学")
print(f"{t.name}, {t.teach()}")  # 输出: 李老师, 教数学
print(t.speak())  # 输出: 我是教数学的老师


# ==============================================================
# 【类方法与静态方法】
# ==============================================================
# 三种方法类型：
#   1. 实例方法：第一个参数是 self，绑定的实例上
#   2. 类方法：第一个参数是 cls，绑定到类本身
#   3. 静态方法：不绑定的普通函数，归属于类的命名空间
#
# 类方法用途：
#   - 工厂方法：创建类的实例
#   - 操作类属性：而不是实例属性
#
# 静态方法用途：
#   - 工具函数：与类相关但不需要访问类或实例
# ==============================================================

print("\n=== 类方法与静态方法 ===")


class MathUtils:
    """数学工具类"""

    # 类属性：π 的近似值
    PI = 3.14159

    def __init__(self):
        # 这里的 __init__ 什么也不做
        # 可以省略，但保持一致的风格
        pass

    # ----------------------------------------------------------
    # 实例方法
    # ----------------------------------------------------------
    # 第一个参数是 self，可以访问实例属性和类属性
    # ----------------------------------------------------------
    def double(self, x):
        """将 x 乘以 2"""
        return x * 2

    # ----------------------------------------------------------
    # @classmethod 装饰器
    # ----------------------------------------------------------
    # 第一个参数是 cls，代表类本身
    # 可以通过 cls 访问类属性和其他类方法
    # 调用方式：类名.方法() 或 实例.方法()（后者会传入类而非实例）
    # ----------------------------------------------------------
    @classmethod
    def circle_area(cls, radius):
        """计算圆面积（使用类属性 PI）"""
        # cls.PI 访问类属性
        return cls.PI * radius * radius

    # ----------------------------------------------------------
    # @staticmethod 装饰器
    # ----------------------------------------------------------
    # 不需要 self 或 cls 参数
    # 只是一个普通的函数碰巧放在类里
    # 适用于与类相关但不依赖类/实例状态的功能
    # ----------------------------------------------------------
    @staticmethod
    def add(a, b):
        """加法（与类无关的纯函数）"""
        return a + b


m = MathUtils()

# 实例方法：通过实例调用
print(f"实例方法 double(5): {m.double(5)}")  # 输出: 10

# 类方法：通过类调用（推荐）或个人调用
print(f"类方法 circle_area(2): {MathUtils.circle_area(2):.2f}")  # 输出: 12.57

# 静态方法：通过类调用
print(f"静态方法 add(3, 4): {MathUtils.add(3, 4)}")  # 输出: 7

# 类属性：直接通过类访问
print(f"类属性 PI: {MathUtils.PI}")  # 输出: 3.14159


# ==============================================================
# 【特殊方法（魔术方法/Dunder Methods）】
# ==============================================================
# 特殊方法以双下划线开头和结尾，如 __init__, __str__
# 也称为"dunder"（double underscore）方法
# Python 会自动调用这些方法，用于实现特定语法功能
#
# 常见分类：
#   - 构造/初始化：__new__, __init__, __del__
#   - 字符串表示：__str__, __repr__
#   - 属性访问：__getattr__, __setattr__, __delattr__
#   - 比较运算：__eq__, __ne__, __lt__, __le__, __gt__, __ge__
#   - 算术运算：__add__, __sub__, __mul__, __neg__, __abs__
#   - 容器类型：__len__, __getitem__, __setitem__, __iter__
#   - 可调用：__call__
#   - 上下文管理：__enter__, __exit__
# ==============================================================

print("\n=== 特殊方法 ===")


class Vector:
    """二维向量类，演示各种特殊方法的用法"""

    # ----------------------------------------------------------
    # 构造方法
    # ----------------------------------------------------------
    def __init__(self, x, y):
        # 存储向量的 x 和 y 分量
        self.x = x
        self.y = y

    # ----------------------------------------------------------
    # 算术运算符重载
    # ----------------------------------------------------------
    # __add__: + 运算符
    # 当执行 v1 + v2 时，Python 调用 v1.__add__(v2)
    # ----------------------------------------------------------
    def __add__(self, other):
        """向量加法：返回一个新的 Vector"""
        return Vector(self.x + other.x, self.y + other.y)

    # __sub__: - 运算符
    def __sub__(self, other):
        """向量减法"""
        return Vector(self.x - other.x, self.y - other.y)

    # __mul__: * 运算符（数乘）
    # 当左操作数不支持时会调用右操作数的 __rmul__
    def __mul__(self, scalar):
        """向量数乘：每个分量乘以标量"""
        return Vector(self.x * scalar, self.y * scalar)

    # __neg__: 一元负运算符
    def __neg__(self):
        """负向量：每个分量取反"""
        return Vector(-self.x, -self.y)

    # ----------------------------------------------------------
    # 比较运算符重载
    # ----------------------------------------------------------
    def __eq__(self, other):
        """== 运算符：判断两个向量是否相等"""
        return self.x == other.x and self.y == other.y

    def __lt__(self, other):
        """< 运算符：按模长比较（用于排序）"""
        # 计算向量模长的平方（避免开方运算）
        return (self.x**2 + self.y**2) < (other.x**2 + other.y**2)

    # ----------------------------------------------------------
    # 一元操作符
    # ----------------------------------------------------------
    def __abs__(self):
        """绝对值/模长：计算向量的长度"""
        # 使用勾股定理计算模长
        return (self.x**2 + self.y**2) ** 0.5

    def __len__(self):
        """长度：返回向量的模（取整）"""
        # 返回模长的整数部分
        return int(self.__abs__())

    # ----------------------------------------------------------
    # 字符串表示
    # ----------------------------------------------------------
    def __str__(self):
        """str() 和 print() 使用的字符串表示"""
        return f"Vector({self.x}, {self.y})"

    def __repr__(self):
        """调试/交互式环境使用的官方表示"""
        return f"Vector({self.x}, {self.y})"

    # ----------------------------------------------------------
    # 索引访问（容器行为）
    # ----------------------------------------------------------
    def __getitem__(self, index):
        """下标访问：v[0] 获取 x，v[1] 获取 y"""
        if index == 0:
            return self.x
        elif index == 1:
            return self.y
        else:
            raise IndexError("索引超出范围")

    def __setitem__(self, index, value):
        """下标赋值：v[0] = 10 修改 x"""
        if index == 0:
            self.x = value
        elif index == 1:
            self.y = value
        else:
            raise IndexError("索引超出范围")

    # ----------------------------------------------------------
    # 可迭代
    # ----------------------------------------------------------
    def __iter__(self):
        """迭代器：使 Vector 可用于 for 循环"""
        # 返回一个迭代器，通常用生成器
        return iter([self.x, self.y])

    # ----------------------------------------------------------
    # 布尔值判断
    # ----------------------------------------------------------
    def __bool__(self):
        """布尔值：零向量返回 False，非零向量返回 True"""
        return self.x != 0 or self.y != 0


# 创建两个向量实例
v1 = Vector(3, 4)  # 模长为 5 的经典向量
v2 = Vector(1, 2)

# 字符串表示
print(f"v1: {v1}")  # Vector(3, 4)
print(f"v2: {v2}")  # Vector(1, 2)

# 算术运算
print(f"v1 + v2: {v1 + v2}")  # Vector(4, 6)
print(f"v1 - v2: {v1 - v2}")  # Vector(2, 2)
print(f"v1 * 2: {v1 * 2}")  # Vector(6, 8)

# 比较运算
print(f"v1 == v2: {v1 == v2}")  # False
print(f"v1 < v2: {v1 < v2}")  # False（因为 25 > 5）

# 一元运算
print(f"-v1: {-v1}")  # Vector(-3, -4)
print(f"|v1|: {abs(v1)}")  # 5.0（调用 __abs__）
