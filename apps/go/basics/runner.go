// RunTopic ж №жҚ®дё»йўҳеҗҚз§°иҝҗиЎҢеҜ№еә”жЁЎеқ—гҖӮ
// ж”ҜжҢҒжЁЎзіҠеҢ№й…ҚпјҡеҰӮ "variable" еҢ№й…Қ "variables"пјҢ"func" еҢ№й…Қ "functions" зӯүгҖӮ
func RunTopic(topic string) bool {
	topic = strings.ToLower(strings.TrimSpace(topic))

	switch {
	case matchTopic(topic, "variable", "variables", "еҸҳйҮҸ"):
		fmt.Println("\n--- дё»йўҳпјҡеҸҳйҮҸгҖҒзұ»еһӢдёҺеёёйҮҸ ---")
		RunVariables()
	case matchTopic(topic, "control", "controlflow", "if", "for", "switch", "defer", "жҺ§еҲ¶", "жҺ§еҲ¶жөҒ"):
		fmt.Println("\n--- дё»йўҳпјҡжҺ§еҲ¶жөҒ ---")
		RunControlFlow()
	case matchTopic(topic, "function", "functions", "func", "еҮҪж•°"):
		fmt.Println("\n--- дё»йўҳпјҡеҮҪж•° ---")
		RunFunctions()
	case matchTopic(topic, "collection", "collections", "array", "slice", "slices", "map", "йӣҶеҗҲ", "ж•°з»„", "еҲҮзүҮ"):
		fmt.Println("\n--- дё»йўҳпјҡйӣҶеҗҲзұ»еһӢ ---")
		RunCollections()
	case matchTopic(topic, "struct", "structs", "method", "methods", "з»“жһ„дҪ“", "ж–№жі•"):
		fmt.Println("\n--- дё»йўҳпјҡз»“жһ„дҪ“дёҺж–№жі• ---")
		RunStructsMethods()
	case matchTopic(topic, "interface", "interfaces", "жҺҘеҸЈ"):
		fmt.Println("\n--- дё»йўҳпјҡжҺҘеҸЈ ---")
		RunInterfaces()
	case matchTopic(topic, "package", "packages", "testing", "test", "еҢ…", "жөӢиҜ•"):
		fmt.Println("\n--- дё»йўҳпјҡеҢ…з®ЎзҗҶдёҺжөӢиҜ• ---")
		RunPackages()
	default:
		fmt.Printf("жңӘзҹҘзҡ„еҹәзЎҖдё»йўҳ: %s\n", topic)
		fmt.Println("еҸҜз”Ёдё»йўҳ: variables, controlflow, functions, collections, structs, interfaces, packages")
		return false
	}
	return true
}

// matchTopic жЈҖжҹҘз”ЁжҲ·иҫ“е…ҘжҳҜеҗҰеҢ№й…ҚжҹҗдёӘдё»йўҳе…ій”®иҜҚгҖӮ
func matchTopic(input string, keywords ...string) bool {
	for _, kw := range keywords {
		if input == kw || strings.Contains(input, kw) || strings.Contains(kw, input) {
			return true
		}
	}
	return false
}
